class Solution:
    # @param A : list of integers
    # @param B : integer
    # @return an integer
    def Sort(self,array):
        n = len(array)

        for i in range(n):
            already_sorted = True

            for j in range(n - i - 1):
                if array[j] > array[j + 1]:
                    array[j], array[j + 1] = array[j + 1], array[j]
                    already_sorted = False
            if already_sorted:
                break

        return array[::-1]

    def solve(self, A, B):
        a_sort = self.Sort(A)
        summ = 0
        for i in range(B):
            summ += a_sort[i]
        return summ


